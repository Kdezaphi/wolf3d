#include "ft_printf.h"

double	ft_sqrt(double d)
{
	int		i;
	double	r;

	i = 1;
	while (i * i <= d)
		i++;
	r = i;
	i = -1;
	while (++i < 100)
		r += ((d - r * r) / (2. * r));
	return (r);
}
