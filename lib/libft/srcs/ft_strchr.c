#include <stdlib.h>
#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;
	size_t	len;

	i = -1;
	len = ft_strlen(s) + 1;
	while (++i < len)
		if (s[i] == (char)c)
			return ((char *)&s[i]);
	return ((char *)NULL);
}
